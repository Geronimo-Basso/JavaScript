let factorial_for = 1
for (let i = 10; i > 0; i--) {
    factorial_for *= i
}
console.log(factorial_for) // Factorial de 10 = 3628800
//-------------------------------------------------------------------------------------------------------------------------------------
let factorial_while = 1
let i = 10
while( i>0 ){
    factorial_while *= i;
    i--;
}
console.log(factorial_while) // Factorial de 10 = 3628800
//-------------------------------------------------------------------------------------------------------------------------------------
let factorial_break = 1
let num = 10
while (true) {
    factorial_break *= num
    num--
    if (num === 1) break
}
console.log(factorial_break) // Factorial de 10 = 3628800
